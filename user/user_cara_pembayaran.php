        -Kunjungi website kami di www.zakatonline.com<br />
        -Setelah muncul tampilan halaman zakat online,silahkan anda login sebagai member.<br />
        -Jika belum anda bisa melakukan pendaftaran sebagai member disini.<br />
        -Setelah anda mamasuki halaman member,silahkan anda masuk ke "PEMBAYARAN".<br />
        -Klik link "bayarkan zakat anda" setelah itu pilih option zakat yang akan anda bayarkan.<br />
        -Klik submit query.<br />
        -Anda akan di hadapkan pada fitur kalkulator zakat sesuai dengan option zakat yang anda pilih.<br />
        -Setelah anda selesai melakukan perhitungan klik selesai.<br />
        -Setelah itu isi data anda pada form pembayaran,selanjutnya klik kirim.<br />

    
      <br />
