      <font   face="Arial, Helvetica, sans-serif"><b>1998</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      		Abu Syauqi, salah satu tokoh da'i muda Bandung,bersama beberapa<br />
            rekan di kelompok pengajian Majlis Ta'lim Ummul Quro sepakat<br />
            membentuk lembaga sosial yang concern pada bantuan kemanusiaan.<br />
            2 Juli 1998, terbentuklah organisasi bernama Dompet Sosial Ummul Quro (DSUQ).<br />
            Sekretariat bertempat di Jl. Turangga 33 Bandung sekaligus sebagai tempat kajian.<br />
            Jamaah pengajian semakin berkembang. Dipergunakanlah Masjid Al Manaar<br />
            Jl. Puter Bandung sebagai tempat kajian rutin. 
      </font>
      <br />
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>1999</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Dukungan masyarakat yang terus meluas mendorong dilakukannya pengelolaan<br />
      organisasi ini lebih baik. Kantor sekretariat pindah ke Jl. Dederuk 30 Bandung.<br />
      Mendekat ke forum pengajian di Masjid Al Manaar. Pencapaian donasi<br />
      selama 1998-1999 terkumpul sebanyak Rp 0,8 Milyar. 
      <br /></font>
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2000</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Animo masyarakat pada perlunya organisasi kemanusiaan semakin meningkat.<br />
      Masyarakat memandang penting misi sosial ini diteruskan bahkan untuk<br />
      kiprah yang lebih luas . Dirintislah program bea siswa pendidikan yatim dan dhuafa,<br />
      layanan kesehatan, rehabilitasi masyarakat miskin kota, dll. Pemekaran mulai<br />
      dilakukan dengan membuka kantor cabang Yogyakarta, Mei 2000 di Jl. Veteran 9.<br />
      Cabang Bandung dipindah ke sekretariat awal di Jl. Turangga 33 Bandung.<br />
      Donasi selama setahun terkumpul Rp 2,1 Milyar.<br />
      </font>
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2001</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Februari, Kantor cabang Jakarta resmi berdiri di Jl. Ekor Kuning Rawamangun,<br />
      Jaktim. Pengumpulan donasi terbukukan sebesar Rp 2,19 Milyar.<br /></font>
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2002</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Identitas lembaga sebagai lembaga amil zakat semakin dikuatkan.<br />
      Kantor Cabang Jakarta pindah ke Jl. Taruna 43 Pulogadung.<br />
      Penerimaan donasi meningkat menjadi Rp 4,19 M.<br /></font>
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2003</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      DSUQ berubah nama menjadi Zakat Online Indonesia DSUQ seiring<br />
      dengan turunnya SK Menteri Agama RI No. 157 pada tanggal 18 Maret 2003<br />
      yang mensertifikasi organisasi ini sebagai Lembaga Amil Zakat Nasional.<br />
      Bulan Mei, Zakat Online Indonesia DSUQ hadir di ibukota Jawa Timur, Surabaya.<br />
      Perolehan donasi terus meningkat menjadi Rp 6,46 M.<br /></font>
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2004</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Kantor cabang Tangerang berdiri. Ekspansi mulai melebar ke Sumatera<br />
       dengan didirikannya kantor cabang Pekanbaru, Riau.<br />
       Dimulainya pembangunan sistem Teknologi Informasi untuk peningkatan<br />
       mutu pelayanan. Hampir seluruh kantor cabang telah tersambung secara online.<br />
       Website www.rumahzakat.org dirilis, menggantikan alamat situs sebelumnya<br />
       di www.rumahzakat.net. Menguatkan branding lembaga dengan nama<br />
       Zakat Online Indonesia.Kepercayaan masyarakat semakin tumbuh,<br />
       donasi terkumpul sebanyak Rp 8,92 M.<br /></font>
       <br />
       <font   face="Arial, Helvetica, sans-serif"><b>2005</b></font><br />
       <font   face="Arial, Helvetica, sans-serif">
       Pertumbuhan cabang meningkat pesat. Tsunami Aceh yang terjadi<br />
       26 Desember 2004 membuka akses Zakat Online Indonesia lebih berperan di Sumatera.<br />
       Cabang-cabang baru pun dibuka : cabang Aceh, Medan, Padang, Palembang,<br />
       Batam berdiri. Di Jawa, berdiri pula kantor cabang Semarang, ditambah<br />
       jaringan kantor cabang pembantu di Bekasi, Bogor, Depok, Jakarta Selatan,<br />
       Cirebon, Solo. Cabang Pekanbaru juga berekspansi dengan memiliki kantor cabang<br />
       pembantu Duri dan Dumai. Sistem informasi lembaga mulai masuk ke jaringan on line.<br />
       Mulai transaksi online, absensi on line, dan beberapa software keuangan.<br />
       Penerimaan donasi meningkat tajam khususnya dari bantuan masyarakat untuk program<br />
       rehabilitasi pasca tsunami Aceh, tercatat Rp 45,26 M donasi terkumpulkan.<br /></font>
       <br />
       <font   face="Arial, Helvetica, sans-serif"><b>2006</b></font>
      <font   face="Arial, Helvetica, sans-serif"><br />
      Regenerasi puncak pimpinan diestafetkan dari Ustadz Abu Syauqi<br />
      beralih ke Virda Dimas Ekaputra. Babak sejarah baru ' Transformation<br />
      From Traditional Corporate to Professional Corporate ' dimulai.<br />
      Kesadaran berzakat terus didorong dengan merilis kampanye<br />
      “When Zakat Being Lifestyle” Diluncurkanlah program Gelar Budaya Zakat (GBZ)<br />
      Menuju Indonesia Sadar Zakat 2008 pertama kali di 6 kota.<br />
      Donasi berhasil terkumpul sebanyak Rp 29,52 M. </font>
      <br />
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2007</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Pengembangan progam semakin disempurnakan termasuk dengan mengganti<br />
      istilah Departemen Empowering menjadi Direktorat Program.Implementasi <br />
      program mulai difokuskan hingga mengerucut pada empat induk yaitu<br />
      EduCare, HealthCare, YouthCare, dan EcoCare.Pengelolaan program dilakukan<br />
      dengan konsep terintergrasi dan berkelanjutan berbasis komunitas.<br /><br />

      ICD merupakan tempat yang difokuskan untuk penyaluran yang terintegrasi yakni<br />
      pendidikan, kesehatan, pelatihan kepemudaan, dan pemberdayaan ekonomi secara<br />
      terpadu berbasis komunitas. Dengan Mustahik Relation Officer sebagai SDM pendamping,<br />
      ICD menjadi pusat penyaluran program sehingga lebih terukur, dan terkontrol.<br />
      Di tahun ini pula Zakat Online Indonesia melebarkan layanan program pendidikan<br />
      dengan menyelenggarakan Sekolah Dasar Juara yang bersifat gratis.<br />
      Guru-guru terbaik dipilih untuk mendidik calon pemimpin bangsa di sana.<br />
      Program komunikasi dikembangkan lebih massif melalui televisi.<br />
      Diluncurkanlah TV Commercial perdana berjudul “Saya Percaya Zakat Online”<br />
      menggandeng endorser Helmy Yahya. Acara Gelar Budaya Zakat (GBZ)<br />
      Menuju Indonesia Sadar Zakat 2008 kembali digelar, kali ini diselenggarakan di 10 kota.<br />
      Ternyata hasil komunikasi dan focusing program bekorelasi positif terhadap<br />
      pencapaian donasi, terkumpul Rp 50,16 M. Triple digit growth! <br /></font>
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2008</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Zakat Online Indonesia berkeinginan kuat untuk memantapkan program-program pemberdayaan.<br />
      Dukungan dan kepercayaan masyarakat menguatkan lembaga untuk semakin fokus kepada<br />
      sebuah rekayasa peradaban besar yang sejak awal telah diimpikan, yakni ”transformasi mustahik<br />
      ke muzakki”. Wujud nyata usaha lembaga adalah dengan meluaskan jaringan pengembangan usaha<br />
      kecil dan mikro di 18 kota. Tidak hanya itu, Zakat Online Indonesia pun menyelenggarakan<br />
      pelatihan-pelatihan motivasi dan ketrampilan dalam wadah Youth Development Center.<br />
      Pelatihan motivasi ini memegang peranan penting karena karakter, pola pikir,<br />
      dan sikap yang kontra produktif menyumbangkan andil besar dalam kelanggengan sebuah kemiskinan.<br />
      Dan yang tidak kalah penting adalah pendampingan masyarakat dilakukan oleh 28 Mustahik Relation<br />
      Officer (MRO) dengan didukung para relawan. Pembelajaran untuk menjadi organisasi yang amanah<br />
      dan professional terus dilakukan, salah satunya dengan penguatan program-program Human Capital.<br />
      Diluncurkanlah program seperti EAZI (Executive Amil Zakat Indonesia), ADP (Amil Development Program),<br />
      ACTPRO (Acceleration Program) dan sebagainya. Kegiatan peningkatan kapasitas ini terbukti efektif<br />
      kompetensi memenuhi tuntutan profesi dan masyarakat. Kepercayaan terus tumbuh, dari pencapaian<br />
      donasi berhasil terkumpulkan donasi sebesar Rp 71,40 Milyar. Untuk memberikan edukasi lebih luas<br />
      kepada masyarakat tentang zakat dan filantropi, Roadshow Gelar Budaya Zakat dilakukan,<br />
      kali ini hadir di 19 Kota. <br /></font>
      <br />
      <font   face="Arial, Helvetica, sans-serif"><b>2009</b></font><br />
      <font   face="Arial, Helvetica, sans-serif">
      Tahun ini menjadi tahun pertama pasca 10 tahun pertama milestone Zakat Online Indonesia.<br />
      Guna penguatan organisasi dikokohkanlah organisasi baru pemberdayaan, yaitu :<br />
      Rumah Sehat Indonesia (pengelola program kesehatan), Rumah Juara Indonesia (pengelola program<br />
      pendidikan), Rumah Mandiri Indonesia (pengelola program kemandirian ekonomi).<br />
      Peningkatan jumlah unit layanan terus dilakukan. Hingga akhir tahun telah berdiri 8 Sekolah Juara,<br />
      7 Rumah Bersalin Gratiis. Tahun 2009 bisa disebut sebagai tahun ekspansi mengingat dalam<br />
      1 semester langsung dibuka 14 cabang baru sehingga menambah total jumlah jaringan sebanyak 45 kantor.<br />
      Pengelolaan yang semakin baik mendapat apresiasi dari masyarakat antara lain award dari<br />
      Karim Business Consulting yang menempatkan Zakat Online Indonesia sebagai #2 LAZNAS Terbaik dalam<br />
      ISR Award (Islamic Social Responsibility Award 2009). Penghargaan juga datang dari IMZ<br />
      (Indonesia Magnificence of Zakat) yang menganugerahi Zakat Online Indonesia sebagai<br />
      The Best Organization in Zakat Development. Pencapaian donasi tumbuh semakin baik, tercatat Rp 107,<br />
      3 Milyar berhasil dikumpulkan dan menjadikan Zakat Online Indonesia sebagai Organisasi Pengelola <br />
      Zakat terbesar pengumpulan donasinya se-Indonesia.<br /></font>
      <br />
      <font  face="Arial, Helvetica, sans-serif"><b>2010</b></font><br />
      <font  face="Arial, Helvetica, sans-serif">
      Krisis global 2009 banyak diprediksikan mulai pulih pada tahun ini, namun tantangan sosial<br />
      dan ekonomi tak lebih mudah dihadapi. Zakat Online Indonesia menyikapi hal ini dengan melakukan<br />
      rangkaian adaptasi dan perubahan menuju organisasi berskala global. 5 April 2010,<br />
      resmi diluncurkanlah brand baru Zakat Online menggantikan brand sebelumnya Zakat Online INDONESIA.<br />
      Dengan mengusung tiga brand value baru : Trusted, Progressive dan Humanitarian, organisasi ini<br />
      menajamkan karakter menuju “World Class Socio-Religious Non Governance Organization (NGO)”.<br />
      Sharing Confidence diangkat menjadi positioning. “Dengan keyakinan yang kuat untuk berbagi<br />
      dan menciptakan keluarga global yang lebih baik, Zakat Online berdaya upaya untuk menjadi<br />
      organisasi terdepan di region yang menjamin program efektif dan berkesinambungan dalam memberdayakan<br />
      masyarakat untuk mencapai kehidupan yang lebih baik.” Untuk memperkuat perubahan ini diluncurkan pula<br />
      gerakan Merangkai Senyum Indonesia, sebuah rangkaian kegiatan untuk memperbaiki<br />
      Indeks Pembangunan Manusia (IPM) Indonesia jauh lebih khususnya dalam bidang pendidikan,<br />
      kesehatan dan kelayakan hidup.<br /></font>
