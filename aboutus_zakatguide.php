	  Zakat adalah harta yang wajib dikeluarkan apabila telah memenuhi syarat-syarat<br>
      yang telah ditentukan oleh agama, dan disalurkan kepada orang-orang yang telah<br>
      ditentukan pula, yaitu delapan golongan yang berhak menerima zakat sebagaimana yang<br>
      tercantum dalam Al-Qur'an surat At-Taubah ayat 60 :<br>
      <br>

	  &quot;Sesungguhnya zakat-zakat itu, hanyalah untuk orang-orang fakir,<br>
      orang-orang miskin, pengurus-pengurus zakat, para mu'allaf yang dibujuk hatinya,<br>
      untuk  budak, orang-orang yang berhutang, untuk jalan Allah dan untuk mereka<br>
      yang sedang dalam perjalanan, sebagai suatu ketetapan yang diwajibkan Allah,<br>
      dan Allah Maha Mengetahui lagi Maha Bijaksana .&quot;<br>
      <br>

	  Zakat dalam bahasa Arab mempunyai beberapa makna :<br><br>

	  Pertama, zakat bermakna At-Thohuru, yang artinya membersihkan atau mensucikan.<br>
      Makna ini menegaskan bahwa  orang yang selalu menunaikan zakat karena Allah<br>
      dan bukan karena ingin dipuji manusia, Allah akan membersihkan dan mensucikan<br>
      baik hartanya maupun jiwanya. Allah SWT berfirman dalam surat At-Taubah ayat 103:<br><br>

	  &quot;Ambillah zakat dari sebagian harta mereka, dengan zakat itu kamu membersihkan<br>
      dan mensucikan  mereka  dan mendo'alah untuk mereka. Sesungguhnya do'a kamu itu<br>
      ketenteraman jiwa bagi mereka. Dan Allah Maha Mendengar lagi Maha Mengetahui.&quot;<br>
      <br>

	  Kedua, zakat bermakna Al-Barakatu, yang artinya berkah.<br>
      Makna ini menegaskan bahwa orang yang selalu membayar zakat,<br>
      hartanya akan selalu dilimpahkan keberkahan oleh Allah SWT,<br>
      kemudian keberkahan harta ini akan berdampak kepada keberkahan hidup.<br>
      Keberkahan ini lahir karena harta yang kita gunakan adalah harta yang suci dan bersih,<br>
      sebab harta kita telah dibersihkan dari kotoran dengan menunaikan zakat yang hakekatnya<br>
      zakat itu sendiri berfungsi  untuk membersihkan dan mensucikan harta.<br><br>

	  Ketiga, zakat bermakna An-Numuw, yang artinya tumbuh dan berkembang.<br>
      Makna ini menegaskan bahwa orang yang selalu menunaikan zakat, hartanya<br>
      (dengan izin Allah) akan selalu terus tumbuh dan berkembang.Hal ini disebabkan oleh<br>
      kesucian dan keberkahan harta yang telah ditunaikan kewajiban zakatnya.<br>
      Tentu kita tidak pernah mendengar orang yang selalu menunaikan zakat dengan<br>
      ikhlas karena Allah, kemudian banyak mengalami masalah dalam harta dan usahanya,<br>
      baik itu kebangkrutan, kehancuran, kerugian usaha, dan lain sebagainya.<br>
      Tentu kita tidak pernah mendengar hal seperti itu, yang ada bahkan sebaliknya.<br><br>

	  Selama beraktivitas di Lembaga Amil Zakat, sampai saat ini penulis belum menemukan<br>
      orang-orang yang rutin menunaikan zakat kemudian berhenti dari menunaikan zakat<br>
      disebabkan usahanya bangkrut atau ekonominya bermasalah, bahkan yang ada adalah<br>
      orang-orang yang selalu menunaikan zakat, jumlah nominal zakat yang dikeluarkannya<br>
      dari waktu ke waktu semakin bertambah besar, itulah bukti bahwa zakat sebenarnya<br>
      tidak mengurangi harta kita, bahkan sebaliknya. Memang secara logika manusia,<br>
      dengan membayar zakat maka harta kita akan berkurang, misalnya jika kita mempunyai<br>
      penghasilan Rp. 2.000.000,- maka zakat yang kita keluarkan adalah 2,5 % dari<br>
      Rp. 2.000.000,- yaitu Rp 50.000,-. Jika kita melihat menurut logika manusia,<br>
      harta yang pada mulanya berjumlah Rp.2.000.000,- kemudian dikeluarkan Rp. 50.000,-<br>
      maka harta kita menjadi Rp. 1.950.000,-  yang berarti jumlah harta kita berkurang.<br>
      Tapi, menurut ilmu Allah yang Maha Pemberi rizki, zakat yang kita keluarkan tidak<br>
      mengurangi harta kita, bahkan menambah harta kita dengan berlipat ganda. <br>
      Allah SWT berfirman dalam surat Ar-Rum ayat 39 :<br><br>

	  &quot;Dan sesuatu riba  yang kamu berikan agar dia bertambah pada harta manusia,<br>
      maka riba itu tidak menambah pada sisi Allah. Dan apa yang kamu berikan berupa zakat yang kamu<br>
      maksudkan untuk mencapai keridhaan Allah, maka  itulah orang-orang yang melipat gandakan .&quot;<br>
      <br>

	  Dalam ayat ini Allah berfirman tentang zakat yang sebelumnya didahului dengan firman tentang riba.<br>
      Dengan ayat ini Allah Maha Pemberi Rizki menegaskan bahwa riba tidak akan pernah melipat gandakan<br>
      harta manusia, yang sebenarnya dapat melipat gandakannya adalah dengan menunaikan zakat.<br><br>

	  Keempat, zakat bermakna As-Sholahu, yang artinya beres atau keberesan, yaitu bahwa orang-orang<br>
      yang selalu menunaikan zakat, hartanya akan selalu beres dan jauh dari masalah.<br>
      Orang yang dalam hartanya selalu ditimpa musibah atau  masalah, misalnya kebangkrutan,<br>
      kecurian, kerampokan, hilang, dan lain sebagainya boleh jadi karena mereka selalu melalaikan<br>
      zakat yang merupakan kewajiban mereka dan hak fakir miskin beserta golongan lainnya yang<br>
      telah Allah sebutkan dalam Al-Qur'an.      </font>
